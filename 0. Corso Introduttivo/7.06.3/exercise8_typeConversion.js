let x = true;

console.log(x);   // Stampa "true"
console.log(typeof(x));   // Stampa "boolean"

x = Number(x);

console.log(x);   // Stampa "1"
console.log(typeof(x));   // Stampa "number"

x = String(x);

console.log(x);   // Stampa "1"
console.log(typeof(x));   // Stampa "string"

x = Boolean(x);

console.log(x);   // Stampa "true"
console.log(typeof(x));   // Stampa "string"